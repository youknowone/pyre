#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn generate_from_graph_produces_function_table() {
        let parsed = crate::parse::parse_source(
            r#"
            fn add(a: i64, b: i64) -> i64 { a + b }
            fn sub(a: i64, b: i64) -> i64 { a - b }
        "#,
        );
        let program = crate::front::build_semantic_program(&parsed);
        let pipeline =
            crate::passes::analyze_program(&program, &crate::passes::PipelineConfig::default());
        let code = generate_from_graph(&pipeline);

        assert!(
            code.contains("GRAPH_FUNCTIONS"),
            "should contain GRAPH_FUNCTIONS table"
        );
        assert!(code.contains("add"), "should list 'add' function");
        assert!(code.contains("sub"), "should list 'sub' function");
        assert!(
            code.contains("graph pipeline"),
            "should have graph pipeline header"
        );
    }

    #[test]
    fn generate_from_pipeline_emits_canonical_header() {
        let mut pipeline = crate::passes::ProgramPipelineResult {
            functions: Vec::new(),
            opcode_dispatch: vec![crate::passes::PipelineOpcodeArm {
                selector: crate::OpcodeDispatchSelector::Path(crate::CallPath::from_segments([
                    "Instruction",
                    "Add",
                ])),
                classified_pattern: None,
            }],
            total_blocks: 0,
            total_ops: 0,
            total_vable_rewrites: 0,
        };
        pipeline.functions.push(crate::passes::PipelineResult {
            name: "add".into(),
            original_blocks: 1,
            annotations_count: 0,
            concrete_types_count: 0,
            vable_rewrites: 0,
            calls_classified: 0,
            transform_notes: Vec::new(),
            classified_pattern: None,
            flattened: crate::passes::FlattenedFunction {
                name: "add".into(),
                ops: Vec::new(),
                num_values: 0,
                num_blocks: 0,
                value_kinds: std::collections::HashMap::new(),
            },
        });
        let code = generate_from_pipeline(&pipeline);
        assert!(code.contains("canonical pipeline"));
        assert!(code.contains("GRAPH_FUNCTIONS"));
        assert!(code.contains("CANONICAL_TRACE_PATTERNS"));
    }
}
