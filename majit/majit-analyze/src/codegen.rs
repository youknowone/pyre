//! Tracing code generation from analysis results.
//!
//! Generates Rust source code that implements tracing for each opcode.
//! The generated code is included by pyre-mjit's build.rs via include!().
//!
//! Each recognized TracePattern produces a tracing function that emits
//! the equivalent IR ops. Unrecognized patterns fall back to residual calls.

use crate::AnalysisResult;

/// Generate tracing code from graph pipeline results.
///
/// This is the graph-based replacement for `generate()`. Instead of
/// using TracePattern string heuristics, it uses the semantic graph
/// pipeline's FlattenedFunction output.
pub fn generate_from_graph(result: &crate::passes::ProgramPipelineResult) -> String {
    let mut out = String::new();
    out.push_str("// Auto-generated by majit-analyze graph pipeline — do not edit.\n");
    out.push_str(&format!(
        "// Graph analysis: {} functions, {} blocks, {} flat ops, {} vable rewrites\n\n",
        result.functions.len(),
        result.total_blocks,
        result.total_ops,
        result.total_vable_rewrites,
    ));

    // Function summary
    out.push_str("/// Graph analysis function summary.\n");
    out.push_str("pub const GRAPH_FUNCTIONS: &[(&str, usize, usize)] = &[\n");
    for func in &result.functions {
        out.push_str(&format!(
            "    (\"{}\", {}, {}),\n",
            func.name, func.original_blocks, func.flattened.ops.len()
        ));
    }
    out.push_str("];\n\n");

    out
}

/// Generate tracing Rust source code from analysis results (legacy).
pub fn generate(result: &AnalysisResult) -> String {
    let mut out = String::new();
    out.push_str("// Auto-generated by majit-analyze — do not edit manually.\n");
    out.push_str(&format!(
        "// Analysis: {} opcodes, {} classified\n\n",
        result.opcodes.len(),
        result
            .opcodes
            .iter()
            .filter(|a| a.trace_pattern.is_some())
            .count(),
    ));

    // Type layout constants (field offsets discovered from source)
    generate_type_layouts(&mut out, result);

    // Named offset constants
    generate_offset_constants(&mut out, result);

    // Helper function classifications
    generate_helper_summary(&mut out, result);

    // Dispatch table
    generate_dispatch_table(&mut out, result);

    // Trace helper functions
    generate_trace_functions(&mut out);

    // Summary
    let classified = result
        .opcodes
        .iter()
        .filter(|a| a.trace_pattern.is_some())
        .count();
    out.push_str(&format!(
        "// Analysis summary: {}/{} opcodes classified, {} helpers, {} trait impls\n",
        classified,
        result.opcodes.len(),
        result.helpers.len(),
        result.trait_impls.len(),
    ));

    out
}

fn generate_type_layouts(out: &mut String, result: &AnalysisResult) {
    out.push_str("// ── Discovered type layouts ──\n");
    out.push_str(&format!("// {} types found\n", result.type_layouts.len()));
    for layout in &result.type_layouts {
        out.push_str(&format!("// struct {} {{\n", layout.name));
        for (i, field) in layout.fields.iter().enumerate() {
            out.push_str(&format!("//   [{}] {}: {}\n", i, field.name, field.ty));
        }
        out.push_str("// }\n");
    }
    out.push('\n');
}

fn generate_offset_constants(out: &mut String, result: &AnalysisResult) {
    if result.offset_constants.is_empty() {
        return;
    }
    out.push_str("// ── Offset constants from interpreter source ──\n");
    out.push_str(&format!(
        "// {} constants discovered\n",
        result.offset_constants.len()
    ));
    for (name, expr) in &result.offset_constants {
        out.push_str(&format!("// {name} = {expr}\n"));
    }
    out.push('\n');
}

fn generate_helper_summary(out: &mut String, result: &AnalysisResult) {
    use crate::classify::HelperClassification;

    let mut field_reads = Vec::new();
    let mut constructors = Vec::new();
    let mut type_checks = Vec::new();

    for helper in &result.helpers {
        match &helper.classification {
            HelperClassification::FieldRead {
                struct_name,
                field_name,
            } => {
                field_reads.push(format!(
                    "// FieldRead: {} → {}.{}",
                    helper.name, struct_name, field_name
                ));
            }
            HelperClassification::Constructor { struct_name } => {
                constructors.push(format!(
                    "// Constructor: {} → New({})",
                    helper.name, struct_name
                ));
            }
            HelperClassification::TypeCheck { type_name } => {
                type_checks.push(format!("// TypeCheck: {} → is_{}", helper.name, type_name));
            }
            _ => {}
        }
    }

    out.push_str("// ── Helper function classifications ──\n");
    out.push_str(&format!(
        "// {} field reads, {} constructors, {} type checks\n",
        field_reads.len(),
        constructors.len(),
        type_checks.len()
    ));
    for s in &field_reads {
        out.push_str(s);
        out.push('\n');
    }
    for s in &constructors {
        out.push_str(s);
        out.push('\n');
    }
    for s in &type_checks {
        out.push_str(s);
        out.push('\n');
    }
    out.push('\n');
}

fn generate_dispatch_table(out: &mut String, result: &AnalysisResult) {
    out.push_str("/// Trace pattern classification for each opcode.\n");
    out.push_str("pub const TRACE_PATTERNS: &[(&str, &str)] = &[\n");
    for arm in &result.opcodes {
        let pattern_str = match &arm.trace_pattern {
            Some(p) => format!("{:?}", p),
            None => "Unclassified".to_string(),
        };
        out.push_str(&format!(
            "    (\"{}\", \"{}\"),\n",
            arm.pattern.replace('"', "\\\""),
            pattern_str.replace('"', "\\\""),
        ));
    }
    out.push_str("];\n\n");
}

